					<pre><p>Lunavada (also transliterated as Lunawada) is a town in the Mahisagar, former in the Northern part of Gujarat state of India. </p></pre>
<p>
Lunawada is the administrative headquarters of the Mahisagar district and one of most developing town of central Gujarat. Lunawada was formally a Taluka, administrative subdivision, in the Panchmahal district up to 15 August 2013. The name Lunawada is derived from Luneshwar Mahadev, a temple of Lord Shiva. Lunawada is surrounded by water, the sources being the Panam river, Vasant Sagar, Kishan Sagar, Kanka Talav, Veri, Mahi River and Darkoli Talav lake.</p>
