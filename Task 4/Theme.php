					<p>Places that visitor can visit are: Luneshwar Temple, Ramji Mandir, Hanumaan ni Veri, Kakachiya Triveni Sangam, Panam Bridge, Panam River Check Dam, Fateh Baug, Fuvara Chawk, Indira Gandhi Stadium, Jahavar Garden, and Kalka Mata ni Tekari.</p>
<p>Visitors can also visit places like the Panam Bridge at evening. Another place to visit is the King's Palace of historically significance in the State of Gujarat. Near Lunawada there is a village named Limadiya where the palace is situated.</p>

<h3>Temple</h3>
<p>
    Lunawada is known for the temple of Luneshwar Mahadev. This ancient temple of Lord Shiva has a lot of religious importance as it is said that the Pandavas dwelled at this temple during their stay in the forest. The statue of Lord Shiva Lingam is of white stone found in the quarries of Madhya Pradesh. There is a Saint Kabir Ashram opposite to this Shiva temple. All the walls of this Ashram are engraved with the Dohas of Saint Kabir. Another ancient temple of Bhairavnath Mahadev is situated near the Luneshwar Mahadev temple. This temple has a statue of Bhairav and one of Lord Shiva.
</p>
<p>The next attraction is the temple of Melia Mahadev, which is 2 miles away from Lunawada. The local people come here to pray to fulfill their wishes and desires. A temple named Mitha Ganesh ni deri situated in mandvi bazar (beside pipli fali) is held here.
	</p>		
	
	
	<h2>People</h2>
	<p>The town contains people of every kind of religious persuasion, Hindus, Jains, Sindhis, Muslims and Dawoodi Bohras. It has Hospitals and clinics for surrounding Talukas. The town has facilities like a Gymnasium, a Town hall, theater, Jawahar Garden, public schools, a Religious school, and restaurants.
</p>
<p>Lunawada people have made achievements in fields of business, education, banking, literacy, and health. Lunawada has a large NRI population thriving in different foreign countries like Kuwait, UAE, Australia, Canada, Oman, US, Hong Kong, and Africa
	     </p>
	     
	     
	     <h2>Godhra-Lunawada Railway</h2>
	     	<p>The Godhra-Lunawada Railway was a 2ft 6in/762 mm narrow gauge(NG) line, opened in 1912 and reached Lunawada in 1914. The railway length of 26 miles(41km) and was served from the Bombay, Baroda and Central India Railway (BB&CIR) station Godhra. The railway was owned and managed by the Gujarat Railways Company. The company was taken over by the Bombay, Baroda and Central India Railway(BB&CIR) in 1922. An Agreement was signed 30 Apr 1914 between the Secretary of State for India and Killick, Nixon & Company of Bombay with partners Lowther Robert Windham Forest, Sir Henry Edward Eddleston Procter, Thomas William Berkett, Walter Henry Ogston and Harold Percival Hebblethwaite. The Company shall construct, complete and make ready and fit for opening for public traffic throughout on a route to be selected and determined by the Secretary of State, a railway, from the Bombay, Baroda and Central India Railway Station at Nadiad to Kapadvanj; and a railway from the Bombay, Baroda and Central India Railway Station at Godhra to Lunawada. This Line was closed in 2002 following The Godhra Train Incident which killed 59 people. Currently plans are going on to revive this line as a broad gauge(BG) line with extension upto Abu Road Railway Station via Modasa, Himmatnagar, Khedbrahma & Ambaji.
