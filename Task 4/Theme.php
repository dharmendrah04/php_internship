					<h2>History</h2>
					<p>Lunavada was the capital of Lunavada State, a princely state that predated the town's existence by around 200 years, having been founded in 1225. The state's rulers claimed descent from the Solanki or Chaulukya dynasty; they constituted one of the sixteen branches of the Solanki tribe and were known as the Virpura Solankis. Before Lunavada was founded, the state's capital was the town of Virpur, across the Mahi river to the west of Lunavada.</p>
					
						<div class="articleRight">
						<div class="articleRightInner">
							<img src="https://upload.wikimedia.org/wikipedia/commons/5/53/Lunavada_coin_of_Wakhat_Singhu.jpg" width="200" height="400"/>
						</div>
					Copper Lunavada coin of Wakhat Singhu</a>
					</div>
					
					
					
					<p>According to traditional accounts, the town of Lunavada was founded in 1434 by Bhim Singh, the rana of Virpur. On a hunting trip across the Mahi river, Bhim Singh ended up lost and separated from his companions. He came across an ascetic's hut and, after respectfully greeting the man, was told that, while passing east through the forest, he would see a hare crossing his path. The ascetic told him that he was to found a city on that spot. Bhim Singh did as he was told and saw the hare at a place now marked by the Bhavaneshvari Mata temple; there he built the town. Since the ascetic was a devotee of the god Luneshwar, the rana named the new town Lunavada out of respect.</p>
					<p>James M. Campbell noted that the story of the ascetic and the hare was a common founding legend for cities. He suggested that, instead, the name Lunavada was in honor of Bhim Singh's relative Lavanprasad, the ruler of Dholka. Campbell said that Bhim Singh was probably driven across the Mahi by the growing power of the Dholka kings, and that he chose Lunavada as the site of his new capital because of its strong defensive position. A rugged hill, subsequently fortified, overlooked the town, and a tangled forest behind it offered a safe escape route if necessary.</p>

					<p>Bhim Singh's direct descendants continued to rule Lunavada until around 1600. The first half of the 1500s apparently saw conflicts with the Gujarat Sultanate; Bodi Moghal, a general of Mahmud Begada, conquered nearby Balasinor in 1505, and a disturbance of some sort occurred in 1545. A paper dating to 1586 indicates the territorial extent of Lunavada State at the time: it still included Virpur and its dependent villages, which later came under Balasinor State, as well as some territory in the north conquered from the thakurs of Meghraj, who also belonged to the Virpura Solanki clan. However, the territory to the south of the Panam river, which later became part of Lunavada State, was not yet under Lunavada's control. Instead, it was controlled by the rulers of Godhra as well as a branch of the Solankis based at Jhanor, near Thasra.</p>
					<p>Around 1600, the direct line descended from Bhim Singh died out, and a collateral relative named Kumbho Rano was brought from the village of Gandhari to become king of Lunavada. One of his descendants, Nar Singh, laid the foundation of the historical Lunavada town wall in 1718; four years later, in 1722, he paid a tribute of 80,000 rupees to Haidar Kuli Khan, the Mughal viceroy of Gujarat. Throughout the 1700s, Lunavada State lost territory to the neighbour Balasinor State, but it gained new lands in the south due to the decline of the Godhra chiefs and the Solanki thakurs.</p>
					
						<p>In the census of 1872, Lunavada was recorded as having a population of 9,662, of whom 7,206 were Hindus and 2,456 were Muslims.</p>
					<p>During the British Raj, Lunavada State was one of the princely states under the Rewa Kantha Agency of Bombay Presidency. It was a second class state in the Rewa Kantha Agency. One important ruler was Wakhat Singhhji (1867–1919) a Solanki Rajput Maharana of high lineage. The 1901 census records that the population had a decrease of 28% in the previous decade, due to famine.</p>
					<p>The last ruler of Lunawada was Maharaja Vir Bhadra Singh. The best known historical place near Lunawada is Kaleshwari where there are Pandav chori, foot prints of Bhima, ancient water kund (small bodies of water sometimes sanctified), several vaavs (large wells with accessible steps to the water level) and the Lord Shiva temple.</p>
					<p>Col. HH Maharaja Sri Virbhadrasinhji Ranjitsinjhi 1929/1986, born 8 June 1910 in Lunawada, invested with full ruling powers on 2 October 1930, Member of the Chamber of Princes, married Maharajkumari Manher Kunwari [HH Maharani Kusum Kunwari of Lunawada], daughter of Capt. HH Maharana Raj Saheb Shri Sir Amarsinhji Banesinhji (Gangubha) of Wankaner, and had issue. He died in 1986.</p>	
					
